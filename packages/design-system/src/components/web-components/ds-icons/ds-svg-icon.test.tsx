import { createTestRenderer } from '../__tests__/rendering';
import './ds-svg-icon';

const view = createTestRenderer('ds-svg-icon', (attrs = {}) => (
  <ds-svg-icon aria-hidden="false" {...attrs} />
));

describe('ds-svg-icon', () => {
  it('renders as an SVG', () => {
    const { customElement } = view();
    expect(customElement.firstElementChild.tagName).toBe('svg');
  });

  it('passes through a custom title', () => {
    const customTitle = 'test title';
    const { customElement } = view({ title: customTitle });
    const titleEl = customElement.firstElementChild.querySelector('title');

    expect(titleEl.textContent).toBe(customTitle);
  });

  it('passes through children', () => {
    const customPath =
      'M248 8C111 8 0 119 0 256s111 248 248 248 248-111 248-248S385 8 248 8zm0 448c-110.3 0-200-89.7-200-200S137.7 56 248 56s200 89.7 200 200-89.7 200-200 200zm-80-216c17.7 0 32-14.3 32-32s-14.3-32-32-32-32 14.3-32 32 14.3 32 32 32zm160 0c17.7 0 32-14.3 32-32s-14.3-32-32-32-32 14.3-32 32 14.3 32 32 32zm4 72.6c-20.8 25-51.5 39.4-84 39.4s-63.2-14.3-84-39.4c-8.5-10.2-23.7-11.5-33.8-3.1-10.2 8.5-11.5 23.6-3.1 33.8 30 36 74.1 56.6 120.9 56.6s90.9-20.6 120.9-56.6c8.5-10.2 7.1-25.3-3.1-33.8-10.1-8.4-25.3-7.1-33.8 3.1z';
    const { customElement } = createTestRenderer('ds-svg-icon', () => (
      <ds-svg-icon>
        <path d={customPath} />
      </ds-svg-icon>
    ))();
    const pathEl = customElement.firstElementChild.querySelector('path');

    expect(pathEl).toHaveAttribute('d', customPath);
  });
});
